<?php

namespace Zan\Framework\Utilities\Math;


class DecimalConverter
{
    public static function decTo($num,$to)
    {
        \ZanPHP\Utilities\Math\DecimalConverter::decTo($num,$to);
    }

    public static function toDec($num,$from)
    {
        \ZanPHP\Utilities\Math\DecimalConverter::toDec($num,$from);
    }

    public static function convert($num,$from,$to)
    {
        \ZanPHP\Utilities\Math\DecimalConverter::convert($num,$from,$to);
    }
}